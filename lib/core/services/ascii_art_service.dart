  /// Generate ASCII art for the given [text] using the specified [font].
  static String generate(String text, {String font = 'Standard'}) {
    final upper = text.toUpperCase();
    switch (font) {
      case 'Block':
        return _generateBlock(upper);
      case 'Mini':
        return _generateMini(upper);
      case 'Shadow':
        return _generateShadow(upper);
      case 'Slant':
        return _generateSlant(upper);
      default:
        return _generateStandard(upper);
    }
  }

