/// A single navigable feature in the app.
class FeatureEntry {
  final String label;

  /// Pre-computed lowercase label for search filtering.
  ///
  /// The navigation drawer calls `_filteredFeatures()` on every
  /// keystroke, which previously ran `label.toLowerCase()` on all
  /// 100+ entries per rebuild. Caching the lowercase form here
  /// eliminates ~100 string allocations per keystroke.
  final String searchLabel;
  final IconData icon;
  final WidgetBuilder builder;
  final FeatureCategory category;

  FeatureEntry({
    required this.label,
    required this.icon,
    required this.builder,
    required this.category,
  }) : searchLabel = label.toLowerCase();
}

/// Categories for organizing features in the navigation drawer.
enum FeatureCategory {
  planning('Planning & Views', Icons.calendar_today),
  productivity('Productivity', Icons.trending_up),
  health('Health & Wellness', Icons.favorite),
  finance('Finance', Icons.attach_money),
  lifestyle('Lifestyle', Icons.palette),
  organization('Organization', Icons.folder),
  tracking('Tracking', Icons.analytics);

  final String label;
  final IconData icon;
  const FeatureCategory(this.label, this.icon);
}

